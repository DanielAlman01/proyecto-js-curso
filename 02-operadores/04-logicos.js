// AND, NOT, ORR

/**
 * AND &&
 * Ambas condiciones se deben de cumplir para porder que de true
 */ 

let mayor = true;
let suscrito = true;

console.log('operador and', mayor && suscrito);

/**
 * OR || 
 * Una de las dos condiciones debe de ser verdadera
 */ 

console.log('operador or', mayor || suscrito);

/**
 * NOT !
 * Invierte el valor de false a true o viseversa
 */ 

console.log('operador not', mayor);
let soloCatalogoInfantil = !mayor;
console.log('operador not', soloCatalogoInfantil);
